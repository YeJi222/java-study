package prob02;

public class Goods {
	private String name;
	private int money;
	private int num;
	
	public void setName(String name) {
		this.name = name;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	public String getName() {
		return name;
	}
	public int getMoney() {
		return money;
	}
	public int getNum() {
		return num;
	}
	
}
