package prob01;

public class Member {
	private String id;
	private String name;
	private double point;
	
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public double getPoint() {
		return point;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPoint(double point) {
		this.point = point;
	}
}