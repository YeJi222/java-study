package prob01;

public class Printer {

	public void println(int intVal) {
		System.out.println(intVal);
	}
	public void println(Boolean boolVal) {
		System.out.println(boolVal);
		
	}
	public void println(double doubleVal) {
		System.out.println(doubleVal);
	}
	public void println(String strVal) {
		System.out.println(strVal);
	}

}
